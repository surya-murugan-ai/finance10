#!/usr/bin/env python3
"""
Start the Python FastAPI server for QRT Closure platform
"""

import uvicorn
import sys
import os

# Add current directory to Python path
sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))

if __name__ == "__main__":
    print("Starting QRT Closure Platform - Python/FastAPI Server")
    print("=" * 60)
    
    # Run the FastAPI server
    uvicorn.run(
        "main:app",
        host="0.0.0.0",
        port=8000,
        reload=True,
        log_level="info",
        access_log=True
    )